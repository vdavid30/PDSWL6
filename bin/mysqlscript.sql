CREATE TABLE CM_EPS (
`nombre` varchar(100)  NOT NULL,
`nit` varchar(20)  NOT NULL PRIMARY KEY
) TYPE=InnoDB;


CREATE TABLE `CM_PACIENTES` (
  `id` int(11) NOT NULL,
  `tipo_id` varchar(2) NOT NULL,
  `nombre` varchar(45) NOT NULL,
  `fecha_nacimiento` date NOT NULL,
  `EPS_nit` varchar(20) NOT NULL,
  PRIMARY KEY (`id`,`tipo_id`),
  CONSTRAINT `fk_PACIENTE_EPS_12` FOREIGN KEY (`EPS_nit`) REFERENCES `CM_EPS` (`nit`) ON DELETE NO ACTION ON UPDATE NO ACTION   
) TYPE=InnoDB;


CREATE TABLE `CM_CONSULTAS` (
  `idCONSULTAS` int(11) NOT NULL AUTO_INCREMENT,
  `fecha_y_hora` datetime NOT NULL,
  `resumen` varchar(200) NOT NULL,  
  `costo` int(11) NOT NULL,
  `PACIENTES_id` int(11) NOT NULL DEFAULT '0',
  `PACIENTES_tipo_id` varchar(2) NOT NULL DEFAULT 'cc',
  PRIMARY KEY (`idCONSULTAS`), 
  CONSTRAINT `fk_CONSULTAS_PACIENTES11` FOREIGN KEY (`PACIENTES_id`, `PACIENTES_tipo_id`) REFERENCES `CM_PACIENTES` (`id`, `tipo_id`) ON DELETE NO ACTION ON UPDATE NO ACTION  
) TYPE=InnoDB;





